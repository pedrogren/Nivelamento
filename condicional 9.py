idade = int(input("Insira sua idade"))
if(idade>=0 and idade<=11):
    print("Voce é criança")
if(idade>=12 and idade<=17):
    print("Voce é adolescente")
if(idade>=18 and idade<=59):
    print("Voce é adulto")
if(idade>=59 and idade<=100):
    print("Voce é idoso")
if(idade>=100):
    print("Voce é uma mumia")